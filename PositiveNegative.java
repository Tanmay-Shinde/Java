public class PositiveNegative
{
    void main(int a)
    {
        if(a>0)
        {
            System.out.println(a+" is positive");
        }
        else if(a<0)
        {
            System.out.println(a+" is negative");
        }
        else
        {
            System.out.println("The number being checked is 0");
        }
    }
}
