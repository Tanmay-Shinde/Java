package Loops.Loops_For;
public class InfiniteForLoop
{
    public static void main1()
    {
        for(int i=0;;i++)
        {
            System.out.println("This Is The Infinity Curse!");
            System.out.println("This Will Go On Till It Never Ends....");
        }
    }
    public static void main2()
    {
        for(int i=0;i>=0;i++)
        {
            System.out.println("This Is The Infinity Curse!");
            System.out.println("This Will Go On Till It Never Ends....");
        }
    }
}
