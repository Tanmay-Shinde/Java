public class Alphanumeric_DT
{
    void main()
    {
        int m=9;
        System.out.println(m);
        
        char c='9';
        System.out.println(c);
        char ch = '#';
        System.out.println(ch);
        char x = 97;
        System.out.println(x);
        
        String p="Hello";
        System.out.println(p);
        String s="234";
        System.out.println(s);
        
        boolean k=true;
        System.out.println(k);
        boolean f=false;
        System.out.println(f);
    }
}
